# string, names of employees
name = [ "Sally", "Bob", "Dan" "Stacey"]
# integers, students grades
grades_precentages = [90, 99, 100, 85]
# floats, outside temputature
tempurature = [85.2, 99.5, 60.4, 45.3]
# booleans, will always be either true or false
bools = [TRUE, FALSE, TRUE, TRUE]

# pop
name.pop
puts name

grades_precentages.pop
puts grades_precentages

tempurature.pop
puts tempurature

bools.pop
puts bools

# push
name.push
puts name

grades_precentages.push
puts grades_precentages

tempurature.push
puts tempurature

bools.push
puts bools

# shift
name.shift
puts name

grades_precentages.shift
puts grades_precentages

tempurature.shift
puts tempurature

bools.shift
puts bools

# unshift
name.unshift
puts name

grades_precentages.unshift
puts grades_precentages

tempurature.unshift
puts tempurature

bools.unshift
puts bools
